        private class Triangle {
            public Point PointA { get; set; }
            public Point PointB { get; set; }
            public Point PointC { get; set; }
            public double AB { get; set; }
            public double AC { get; set; }
            public double BC { get; set; }
            public double AngleA { get; set; }
            public double AngleB { get; set; }
            public double AngleC { get; set; }

            public void Solve() {
                this.AB = SolveDistance(this.PointA, this.PointB);
                this.AC = SolveDistance(this.PointA, this.PointC);
                this.BC = SolveDistance(this.PointB, this.PointC);
                SolveAngleA();
                SolveAngleB();
                SolveAngleC();
            }

            public void SolveTest() {
                this.AB = 7;
                this.AC = 6;
                this.BC = 8;
                SolveAngleA();
                SolveAngleB();
                SolveAngleC();
            }

            private void SolveAngleA() {
                double cosA = (Math.Pow(this.AC, 2) + Math.Pow(this.AB, 2) - Math.Pow(this.BC, 2)) / (2 * this.AC * this.AB);
                this.AngleA = Math.Acos(cosA) * 180 / Math.PI;
            }

            private void SolveAngleB() {
                double cosB = (Math.Pow(this.AB, 2) + Math.Pow(this.BC, 2) - Math.Pow(this.AC, 2)) / (2 * this.AB * this.BC);
                this.AngleB = Math.Acos(cosB) * 180 / Math.PI;
            }

            private void SolveAngleC() {
                this.AngleC = 180 - this.AngleA - this.AngleB;
            }

            private double SolveDistance(Point x, Point y) {
                if (x.X == y.X) {
                    return Math.Abs(x.Y - y.Y);
                } else if (x.Y == y.Y) {
                    return Math.Abs(x.X - y.X);
                } else {
                    return Math.Sqrt(Math.Pow(Math.Abs(x.X - y.X), 2) + Math.Pow(Math.Abs(x.Y - y.Y), 2));
                }
            }

            public Triangle(Point a, Point b, Point c) {
                this.PointA = a;
                this.PointB = b;
                this.PointC = c;
            }
        }

