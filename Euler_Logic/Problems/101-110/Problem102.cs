        }

        private bool DoesContainOrigin(Triangle triangle) {
            triangle.Solve();
            _triangleWithOrigin.PointA = triangle.PointA;
            _triangleWithOrigin.PointB = triangle.PointB;
            _triangleWithOrigin.Solve();
            if (_triangleWithOrigin.AngleA <= triangle.AngleA && _triangleWithOrigin.AngleB <= triangle.AngleB) {
                _triangleWithOrigin.PointB = triangle.PointC;
                _triangleWithOrigin.Solve();
                if (_triangleWithOrigin.AngleA <= triangle.AngleA && _triangleWithOrigin.AngleB <= triangle.AngleC) {
                    _triangleWithOrigin.PointA = triangle.PointB;
                    _triangleWithOrigin.Solve();
                    if (_triangleWithOrigin.AngleA <= triangle.AngleB && _triangleWithOrigin.AngleB <= triangle.AngleC) {
                        return true;
                    } else {
                        return false;
                    }
                } else {
                    return false;
                }
            } else {
                return false;
            }
        }

