        public override string GetAnswer() {
            BuildAlphabet();
            LoadTexts();
            return CountTrianglesInTexts().ToString();
        }

        private int CountTrianglesInTexts() {
            int count = 0;
            foreach (string text in _texts) {
                decimal code = GetCode(text);
                if (code > _highestTriangleNum) {
                    CalcTriangleNums(code);
                }
                if (_triangleNums.Contains(code)) {
                    count++;
                }
            }
            return count;
        }

        private void CalcTriangleNums(decimal max) {
            do {
                _highestTriangleIndex++;
                _highestTriangleNum = 0.5M * _highestTriangleIndex * (_highestTriangleIndex + 1);
                _triangleNums.Add(_highestTriangleNum);
            } while (_highestTriangleNum < max);
        }

        private decimal GetCode(string text) {
            int sum = 0;
            for (int index = 0; index < text.Length; index++) {
                sum += _alphabet[text.Substring(index, 1)];
            }
            return sum;
        }

