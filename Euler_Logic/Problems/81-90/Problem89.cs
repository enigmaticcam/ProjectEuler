        public string GetAnswer() {
            LoadAll();
            InitializeRef();
            return FindSumOfSaved().ToString();
        }

        private int FindSumOfSaved() {
            int sum = 0;
            foreach (string roman in _romans) {
                sum += roman.Length - GetRomanOfNUm(GetNumOfRoman(roman)).Length;
            }
            return sum;
        }

        private int GetNumOfRoman(string roman) {
            int lastNum = int.MaxValue;
            int sum = 0;
            for (int index = 0; index < roman.Length; index++) {
                string digit = roman.Substring(index, 1);
                int num = _romanToNum[digit];
                if (num > lastNum) {
                    sum += (num - lastNum - lastNum);
                } else {
                    sum += num;
                }
                lastNum = num;
            }
            return sum;
        }

        private string GetRomanOfNUm(int num) {
            StringBuilder roman = new StringBuilder();
            while (num != 0) {
                if (num >= 1000) {
                    roman.Append("M");
                    num -= 1000;
                } else if (num >= 900) {
                    roman.Append("CM");
                    num -= 900;
                } else if (num >= 500) {
                    roman.Append("D");
                    num -= 500;
                } else if (num >= 400) {
                    roman.Append("CD");
                    num -= 400;
                } else if (num >= 100) {
                    roman.Append("C");
                    num -= 100;
                } else if (num >= 90) {
                    roman.Append("XC");
                    num -= 90;
                } else if (num >= 50) {
                    roman.Append("L");
                    num -= 50;
                } else if (num >= 40) {
                    roman.Append("XL");
                    num -= 40;
                } else if (num >= 10) {
                    roman.Append("X");
                    num -= 10;
                } else if (num >= 9) {
                    roman.Append("IX");
                    num -= 9;
                } else if (num >= 5) {
                    roman.Append("V");
                    num -= 5;
                } else if (num >= 4) {
                    roman.Append("IV");
                    num -= 4;
                } else {
                    roman.Append("I");
                    num -= 1;
                }
            }
            return roman.ToString();
        }

