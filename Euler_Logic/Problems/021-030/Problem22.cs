        private ulong CalcScores() {
            _names.Sort();
            ulong index = 1;
            ulong sum = 0;
            foreach (string name in _names) {
                ulong score = 0;
                for (int a = 0; a < name.Length; a++) {
                    score += _charScores[name.Substring(a, 1)];
                }
                sum += (score * index);
                index++;
            }
            return sum;
        }

